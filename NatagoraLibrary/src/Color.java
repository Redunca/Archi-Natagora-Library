public class Color {
    private long Id;
    private String Color;
    private String ColorRGB;

    public long getId() {
        return Id;
    }

    public void setId(long id) {
        Id = id;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }

    public String getColorRGB() {
        return ColorRGB;
    }

    public void setColorRGB(String colorRGB) {
        ColorRGB = colorRGB;
    }
}
